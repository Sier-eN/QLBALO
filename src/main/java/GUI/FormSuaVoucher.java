    private void btn_capnhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_capnhatActionPerformed
        // TODO add your handling code here:
        VoucherDTO v = new VoucherDTO();
        v.setMaVoucher(lbl_mavoucher.getText());
        v.setTenVoucher(tf_tenvoucher.getText().trim());
        v.setChucNang(cb_chucnang.getSelectedItem().toString());
        
        try {
            v.setGiaTri(Double.parseDouble(tf_giatri.getText().trim()));
            v.setSoLuong(Integer.parseInt(tf_soluong.getText().trim()));
            
            if (v.getGiaTri() < 0 || v.getSoLuong() < 0) {
                JOptionPane.showMessageDialog(this, "Giá trị không được âm!");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng số!");
            return;
        }

        String ketQua = bll.suaVoucher(v);
        JOptionPane.showMessageDialog(this, ketQua);
        
        if (ketQua.contains("thành công")) {
            this.dispose();
        }
    }//GEN-LAST:event_btn_capnhatActionPerformed

