    private boolean checkInput(javax.swing.JTextField tf, String hint, String name) {
        if (tf.getText().trim().isEmpty() || tf.getText().equals(hint)) {
            JOptionPane.showMessageDialog(getFrame(), "Vui lòng nhập " + name + "!");
            tf.requestFocus();
            return true;
        }
        return false;
    }

